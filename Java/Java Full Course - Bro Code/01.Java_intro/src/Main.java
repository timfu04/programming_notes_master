
public class Main {

	public static void main(String[] args) {
		
		// "print" does not add new line automatically
		System.out.print("I love pizza 1");
		System.out.print("It's really good 1!");
		
		System.out.print("\n");
		
		// "println" adds new line automatically
		System.out.println("I love pizza 2");
		System.out.println("It's really good 2!");
		
		// add new line using "\n"
		System.out.print("I love pizza 3\n");
		System.out.print("It's really good 3!\n");
		
		
		// add tab using "\t"
		System.out.println("\t I love pizza 4");
		System.out.println("\t It's really good 4!");
		
		// add backslash to display double quotes
		System.out.println("\"I love pizza 5\"");
		System.out.println("\"It's really good 5!");
		
		// This is a comment
		
		/*
		 * This  
		 * is 
		 * a 
		 * multiple
		 * line
		 * comment
		 */
	}
}


